public class calculadora_26 {
  // Método sobrecargado para sumar enteros
    public int sumar(int a, int b) {
        return a + b;
    }

    // Método sobrecargado para sumar dobles (sobrecarga de métodos)
    public double sumar(double a, double b) {
        return a + b;
    }

    // Método con valor de retorno para multiplicar enteros
    public int multiplicar(int a, int b) {
        return a * b;
    }

    // Método con valor de retorno para multiplicar dobles
    public double multiplicar(double a, double b) {
        return a * b;
    }
}
